def reverse_sort(number_list):
  """
  Reverse the order of the numbers in a list.

  Args:
    number_list: A list of numbers.

  Returns:
    A new list with the numbers from `number_list` sorted in descending order.
  """

  # Reverse the order of the numbers in the list.
  number_list.reverse()

  # Return the reversed list.
  return number_list