def simple_sort(number_list):
  """
  Redonas novan liston kun la nombroj el `number_list` ordigitaj laŭ kreska ordo.

  Args:
    number_list: La liston de nombroj, kiun oni volas ordigi.

  Returns:
    Novan liston kun la nombroj ordigitaj laŭ kreska ordo.
  """
  sorted_list = sorted(number_list)
  return sorted_list